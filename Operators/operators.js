/* Asignation Operators:
    =   x = y   x = y
    +=  x += y  x = x + y
    -=  x -= y  x = x - y
    *=  x *= y  x = x * y
    /=  x /= y  x = x / y
    %=  x %= y  x = x % y
    **= x **= y x = x ** y
*/

let number = 1;
number += 1;
document.write(number + "<br>");
number -= 1;
document.write(number + "<br>");
number *= 5;
document.write(number + "<br>");
number /= 2;
document.write(number + "<br>");
number %= 2;
document.write(number + "<br>");

/* Numeric Operators:
    +   Addition
    -   Subtraction
    *   Multiplication
    /   Division
    %   Modulus
    **  Exponentiation
    --  Decrement
    ++  Increment
*/

let number1 = 1;
let number2 = 2;

let result = number1 + number2;
document.write(result + "<br>");
result++;
document.write(result + "<br>");