/* Math Methods:
- Math.ceil() - Rounds up to the nearest integer
- Math.abs() - Returns the absolute value of a number
- Math.max() - Returns the largest of zero or more numbers
- Math.min() - Returns the smallest of zero or more numbers
- Math.random() - Generates a random number between 0 and 1
- Math.round() - Rounds to the nearest integer
- Math.floor() - Rounds down to the nearest integer
- Math.fround() - Returns the nearest single precision float representation of a number
- Math.trunc() - Returns the integer part of a number by removing any fractional digits
*/


// Math.max()
var max = Math.max(3, 54, 32, 16);
document.write("<br>" + max + "<br>");

// Math.min()
var min = Math.min(3, 54, 32, 16);
document.write("<br>" + min + "<br>");

// Math.random()
var random = Math.random();
document.write("<br>" + random + "<br>");

// Math.round()
var round = Math.round(3.5);
document.write("<br>" + round + "<br>");

// Math.floor()
var floor = Math.floor(3.5);
document.write("<br>" + floor + "<br>");

// Math.fround()
var fround = Math.fround(3.59999999);
document.write("<br>" + fround + "<br>");

// Math.trunc()
var trunc = Math.trunc(3.5);
document.write("<br>" + trunc + "<br>");

/* Properties:
- Math.E - Euler's constant and the base of natural logarithms
- Math.LN2 - Natural logarithm of 2
- Math.LN10 - Natural logarithm of 10
- Math.LOG2E - Base 2 logarithm of E
- Math.LOG10E - Base 10 logarithm of E
- Math.PI - Ratio of the circumference of a circle to its diameter
- Math.SQRT1_2 - Square root of 1/2
- Math.SQRT2 - Square root of 2
*/

// Math.E
var e = Math.E;
document.write("<br>" + e + "<br>");

// Math.LN2
var ln2 = Math.LN2;
document.write("<br>" + ln2 + "<br>");

// Math.LN10
var ln10 = Math.LN10;
document.write("<br>" + ln10 + "<br>");

// Math.LOG2E
var log2e = Math.LOG2E;
document.write("<br>" + log2e + "<br>");

// Math.LOG10E
var log10e = Math.LOG10E;
document.write("<br>" + log10e + "<br>");

// Math.PI
var pi = Math.PI;
document.write("<br>" + pi + "<br>");

// Math.SQRT1_2
var sqrt1_2 = Math.SQRT1_2;
document.write("<br>" + sqrt1_2 + "<br>");

// Math.SQRT2
var sqrt2 = Math.SQRT2;
document.write("<br>" + sqrt2 + "<br>");